// Initialize count with the value 10
let count = 10;

// Pre-increment the variable and log the value
console.log("Pre-increment: " + (++count)); 

// Post-increment the variable and log the value
console.log("Post-increment: " + (count++)); 

// Log the updated value of count after post-increment
console.log("Value after post-increment: " + count); 

// Pre-decrement the variable and log the value
console.log("Pre-decrement: " + (--count));  

// Post-decrement the variable and log the value
console.log("Post-decrement: " + (count--));

// Log the updated value of count after post-decrement
console.log("Value after post-decrement: " + count);
