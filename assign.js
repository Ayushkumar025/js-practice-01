// Initialize x with the value 10
let x = 10;

// Add 5 to x using +=
x += 5;
console.log("After adding 5, x = " + x); // 15

// Subtract 3 from x using -=
x -= 3;
console.log("After subtracting 3, x = " + x); // 12

// Multiply x by 2 using *=
x *= 2;
console.log("After multiplying by 2, x = " + x); // 24

// Divide x by 4 using /=
x /= 4;
console.log("After dividing by 4, x = " + x); // 6

// Find the remainder when x is divided by 2 using %=
x %= 2;
console.log("After finding the remainder when divided by 2, x = " + x); // 0
