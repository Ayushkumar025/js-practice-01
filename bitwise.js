// Perform a bitwise AND on 5 and 3
let andResult = 5 & 3;
console.log("5 & 3: " + andResult); // 1

// Perform a bitwise OR on 7 and 2
let orResult = 7 | 2;
console.log("7 | 2: " + orResult); // 7

// Perform a bitwise XOR on 12 and 6
let xorResult = 12 ^ 6;
console.log("12 ^ 6: " + xorResult); // 10

// Use the NOT operator on 9
let notResult = ~9;
console.log("~9: " + notResult); // -10

// Left shift the value 3 by 2 positions
let leftShiftResult = 3 << 2;
console.log("3 << 2: " + leftShiftResult); // 12

// Right shift the value 10 by 1 position
let rightShiftResult = 10 >> 1;
console.log("10 >> 1: " + rightShiftResult); // 5

// Zero-fill right shift the value -5 by 2 positions
let zeroFillRightShiftResult = -5 >>> 2;
console.log("-5 >>> 2: " + zeroFillRightShiftResult); // 1073741822
